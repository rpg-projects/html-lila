function getTextReady(char, color) {
  const text = document.getElementById("text").value;

  let isLineStart = true;
  const newText = [];

  for (let i = 0; i < text.length; i++) {
    if ((text[i] == "~" || text[i] == "—") && isLineStart) {
      char === "aspen"
        ? newText.push(`<b style="color: ${color}">&quot;`)
        : char === "emily" || char === "brad"
        ? newText.push(`———<b style="color: ${color}">`)
        : newText.push(`—<b style="color: ${color}">`);
      isLineStart = false;
    } else if ((text[i] == "~" || text[i] == "—") && isLineStart == false) {
      char === "aspen"
        ? newText.push(`&quot;</b>`)
        : char === "emily" || char === "brad"
        ? newText.push(`</b>———`)
        : newText.push("</b>—");
      isLineStart = true;
    } else newText.push(text[i]);
  }

  return newText.join("");
}

const getHTMLReady = () => {
  const char = document.getElementById("selectChar").value;
  const char2 = document.getElementById("selectChar2").value;

  if (char !== "none" && char2 !== "none") {
    alert("Inválido");

    document.getElementById("selectChar").value = "none";
    document.getElementById("selectChar2").value = "none";
  }

  let { html, color } = { html: "", color: "" };

  if (char !== "none") {
    ({ html, color } = getCharHTML(char));
  }
  if (char2 !== "none") {
    ({ html, color } = getCharHTML(char2));
  }

  let [part1, part2] = html.split("TEXTO");

  const text = getTextReady(char, color);

  completeHtml = `${part1}${text}${part2}`;

  navigator.clipboard.writeText(completeHtml);
  return completeHtml;
};
