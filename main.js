function getTextReady(char, color) {
  const text = document.getElementById("text").value;

  let isLineStart = true;
  const newText = [];

  for (let i = 0; i < text.length; i++) {
    if ((text[i] == "~" || text[i] == "—") && isLineStart) {
      char === "aspen" || char === "aspen-twh"
        ? newText.push(`<b style="color: ${color}">&quot`)
        : newText.push(`—<b style="color: ${color}">`);
      isLineStart = false;
    } else if ((text[i] == "~" || text[i] == "—") && isLineStart == false) {
      char === "aspen" ? newText.push(`&quot</b>`) : newText.push("</b>—");
      isLineStart = true;
    } else newText.push(text[i]);
  }

  return newText.join("");
}

const getHTMLReady = () => {
  const button = document.getElementById("get-html-button");
  const char = document.getElementById("selectChar").value;
  const char3 = document.getElementById("selectChar3").value;

  if (char !== "none" && char3 !== "none") {
    alert("Inválido");

    document.getElementById("selectChar").value = "none";
    document.getElementById("selectChar3").value = "none";
  }

  let { html, color } = { html: "", color: "" };

  if (char !== "none") {
    ({ html, color } = getCharHTML(char));
  }

  if (char3 !== "none") {
    ({ html, color } = getCharHTML(char3));
  }

  let [part1, part2] = html.split("TEXTO");

  const text = getTextReady(char, color);

  completeHtml = `${part1}${text}${part2}`;

  navigator.clipboard.writeText(completeHtml);

  // 👉 feedback visual no botão
  const originalText = button.innerText;
  button.innerText = "HTML COPIADO ✔";

  setTimeout(() => {
    button.innerText = originalText;
  }, 1000); // volta depois de 1s

  return completeHtml;
};
